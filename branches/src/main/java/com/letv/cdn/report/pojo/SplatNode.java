package com.letv.cdn.report.pojo;
public class SplatNode{
    public String platid;
    public String platname;
    public String splatid;
    public String splatname;
    public String remark;
    
    public String getPlatid() {
        return platid;
    }
    public void setPlatid(String platid) {
        this.platid = platid;
    }
    public String getPlatname() {
        return platname;
    }
    public void setPlatname(String platname) {
        this.platname = platname;
    }
    public String getSplatid() {
        return splatid;
    }
    public void setSplatid(String splatid) {
        this.splatid = splatid;
    }
    public String getSplatname() {
        return splatname;
    }
    public void setSplatname(String splatname) {
        this.splatname = splatname;
    }
    public String getRemark() {
        return remark;
    }
    public void setRemark(String remark) {
        this.remark = remark;
    }
}
